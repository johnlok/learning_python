#quotation manipulation
#working with string methods

#quote from IBM Chairman, Thomas Watson, in 1943
quote = "I think there is a world market for maybe five computers"

print("Original quote: " + quote)

print("In upper case: " + quote.upper())
print("In title case: " + quote.title())
print("with a minor replacement: " + quote.replace("five", "millions of"))
print("Original quote is still " + quote)

